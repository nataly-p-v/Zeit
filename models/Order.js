const mongoose = require('mongoose')
const schema = mongoose.Schema

const orderSchema = new schema({
    date: {
        type: Date,
        default: Date.now
    },
    order: {
        type: Number,
        required: true
    },
    list: [
        {
            name: {
                type: String
            },
            quantity: {
                type: Number
            },
            cost: {
                type: Number
            }
        }
    ],
    user: {
        ref: 'users',
        type: schema.Types.ObjectId
    }
})

module.exports = mongoose.model('orders', orderSchema)